package com.opgkukic.buzzkeeper.model;

public class Ispaša {
    private String img;
    private String naziv;

    public Ispaša() {}

    public Ispaša(String img, String naziv) {
        this.img = img;
        this.naziv = naziv;
    }

    public String getImg() { return img; }
    public String getNaziv() { return naziv; }
}
